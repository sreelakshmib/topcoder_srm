class Workshop {
public:
	int pictureFrames(vector <int>);
};

int Workshop::pictureFrames(vector <int> pieces) {
	int res=0;
	int n=pieces.size();
	vector<int> p=pieces;
	for(int i=0;i<n;i++){
		for(int j=i+1;j<n;j++){
			for(int k=j+1;k<n;k++){
				if(p[i]+p[j]>p[k] && p[j]+p[k]>p[i] && p[k]+p[i]>p[j])
					res++;
			}
		}
	}	
	return res;	
}


