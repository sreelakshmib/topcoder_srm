class PrimeContainers {
public:
	int containerSize(int);
};

bool isPrime(int n)
{
	for(int i=2;i*i<=n;i++)
		if(n%i == 0)
			return false;
	return true;
}
int PrimeContainers::containerSize(int N) {
		int count = 0;
		while(N >1)
		{
			if(isPrime(N)) count++;
			N /= 2;
		}
		return count;
}


