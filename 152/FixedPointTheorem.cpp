class FixedPointTheorem {
public:
	double cycleRange(double);
};

double FixedPointTheorem::cycleRange(double R) {
	double res;
	double x=0.25;
	for(int i=1;i<=200000;i++){
		x=R*x*(1-x);
	}	
	double low=x;
	double high=x;
	for(int i=1;i<=1000;i++){
		x=R*x*(1-x);
		if(low>=x)low=x;
		if(high<=x)high=x;
	}	
	return high-low;
}


