class LetterStrings {
public:
	int sum(vector <string>);
};

int LetterStrings::sum(vector <string> s) {
	int cnt=0;
	for(int i=0;i<s.size();i++){
		for(int j=0;j<s[i].length();j++){
			if(s[i][j]!='-')cnt++;
		}
	}
	return cnt;
}


