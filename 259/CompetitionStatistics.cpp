class CompetitionStatistics {
public:
	int consecutiveGrowth(vector <int>);
};

int CompetitionStatistics::consecutiveGrowth(vector <int> rating) {
	int max=0,cur=0;
	rep(i,sz(rating)){
		if(rating[i]>0){
			cur++;
		} else{
			if(cur>max)
				max=cur;
			cur=0;
		}
	}
	if(cur>max)
		max=cur;
	return max;
}


