class SuperRot {
public:
	string decoder(string);
};

string SuperRot::decoder(string message) {
	for(int i=0;i<message.length();i++){
		if(message[i]==' ')continue;
		if((message[i]>='a' && message[i]<='m') || (message[i]>='A' && message[i]<='M'))
			message[i]+=13;
		else if((message[i]>='n' && message[i]<='z') || (message[i]>='N' && message[i]<='Z'))
			message[i]-=13;
		else if(message[i]>='0' && message[i]<='4')
			message[i]+=5;
		else if(message[i]>='5' && message[i]<='9')
			message[i]-=5;
	}
	return message;
}


