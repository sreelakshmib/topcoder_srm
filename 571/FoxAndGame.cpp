class FoxAndGame {
public:
	int countStars(vector <string>);
};

int FoxAndGame::countStars(vector <string> result) {
	int res=0;
	rep(i,sz(result)){
		rep(j,len(result[0])){
			if(result[i][j]=='o')
				res++;
		}
	}
	return res;
}


