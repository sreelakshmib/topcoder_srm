class WarCry {
public:
	int alertTime(string);
};


int WarCry::alertTime(string outposts) {
	int m=0,c=0;
	rep(i,len(outposts)){
		if(outposts[i]=='-'){
			c++;
		} else if(outposts[i]=='x'){
			if(c>m)m=c;
			c=0;
		}
	}
	c=0;
	int c1=0;
	rep(i,len(outposts)){
		if(outposts[i]=='-')
			c++;
		else
			break;
	}
	reverse(full(outposts));
	rep(i,len(outposts)){
		if(outposts[i]=='-')
			c1++;
		else
			break;
	}
	return max(c,max(c1,(m+1)/2));
}

