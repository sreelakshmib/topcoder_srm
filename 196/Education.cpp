class Education {
public:
	int minimize(string, vector <int>);
};

int Education::minimize(string desire, vector <int> tests) {
	double req=0;
	if(desire=="A")req=90;
	else if(desire=="B")req=80;
	else if(desire=="C")req=70;
	else if(desire=="D")req=60;
	int mark=0;
	int cur=0,n=tests.size();
	for(int i=0;i<tests.size();i++)cur+=tests[i];
	if((cur+100)*1. / (n+1)<req)return -1;
	while(mark<=100){
		if((cur+mark)*1. / (n+1)>=req)return mark;
		mark++;
	}
	return -1;
}


