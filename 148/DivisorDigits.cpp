class DivisorDigits {
public:
	int howMany(int);
	vector<int> digits(int n);
};

vector<int> DivisorDigits::digits(int n){
	vector<int> v;
	while(n){
		if(n%10!=0)v.push_back(n%10);
		n/=10;
	}
	return v;
}

int DivisorDigits::howMany(int number) {
	vector<int> v=digits(number);
	int res=0;
	for(int i=0;i<v.size();i++)
		if(number%v[i]==0)res++;
	return res;
}



