class MovingAverages {
public:
	vector <int> calculate(vector <string>, int);
};

vector <int> MovingAverages::calculate(vector <string> times, int n) {
	vector<int>res;
	int prev=0;
	int a=0;
	for(int i=0;i<times.size()-n+1;i++){
	a=0;
		for(int j=0;j<n;j++){
		stringstream ss;
		int p=times[i+j].find(':');times[i+j][p]=' ';
		p=times[i+j].find(':');times[i+j][p]=' ';
		ss<<times[i+j];
		int h,m,s;
		ss>>h>>m>>s;
		a+=h*3600+m*60+s;
	}
	res.push_back(a/n);
	}
	
	return res;
	
}


