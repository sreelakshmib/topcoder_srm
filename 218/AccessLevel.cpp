class AccessLevel {
public:
	string canAccess(vector <int>, int);
};

string AccessLevel::canAccess(vector <int> rights, int min) {
	string res="";
	for(int i=0;i<rights.size();i++){
		if(rights[i]<min)
			res+='D';
		else 
			res+='A';
	}
	return res;
}


