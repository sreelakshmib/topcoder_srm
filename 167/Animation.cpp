class Animation {
public:
	vector <string> animate(int, string);
};

vector <string> Animation::animate(int speed, string init) {
	int n=init.length();
	string last="";
	for(int i=0;i<n;i++)last+='.';
	vector<string> res;
	while(init!=last){
		res.pb(init);
		string next=last;
		for(int i=0;i<n;i++){
			if((init[i]=='L' || init[i]=='B') && i-speed>=0)
				next[i-speed]='L';
		}
		for(int i=0;i<n;i++){
			if((init[i]=='R' || init[i]=='B') && i+speed<n)
				next[i+speed] = next[i+speed]=='.'?'R':'B';
		}	
		init=next;
	}
	res.pb(last);	
	for(int i=0;i<res.size();i++){
		for(int j=0;j<n;j++){
			if(res[i][j]!='.')
				res[i][j]='X';
		}
	}
	return res;
}


