class RGBColor {
public:
	vector <int> getComplement(vector <int>);
};

vector <int> RGBColor::getComplement(vector <int> rgb) {
	vector<int> res=rgb;
	for(int i=0;i<rgb.size();i++){
		res[i]=255-rgb[i];
	}
	if(abs(res[0]-rgb[0])<=32 && abs(res[1]-rgb[1])<=32 && abs(res[2]-rgb[2])<=32){
		res[0]=rgb[0];		if(res[0]+128<=255)res[0]+=128;
		else res[0]-=128;
		res[1]=rgb[1];
		if(res[1]+128<=255)res[1]+=128;
		else res[1]-=128;
		res[2]=rgb[2];
		if(res[2]+128<=255)res[2]+=128;
		else res[2]-=128;
	}
	return res;	
}


