class PartySeats {
public:
	vector <string> seating(vector <string>);
};

vector <string> PartySeats::seating(vector <string> attendees) {
	vector<string> b;
	vector<string> g;
	vector<string> res;
	for(int i=0;i<attendees.size();i++){
		stringstream s;
		s<<attendees[i];
		string name,gender;
		s>>name>>gender;
		if(gender=="boy")b.push_back(name);
		else g.push_back(name);
	}
	sort(b.begin(),b.end());
	sort(g.begin(),g.end());
	if(b.size()<=1) return res;
	if(g.size()<=1)return res;
	if(g.size()!=b.size())return res;
	if(g.size()%2)return res;
	res.push_back("HOST");
	for(int i=0;i<g.size()/2;i++){
		res.push_back(g[i]);
		res.push_back(b[i]);
	}
	res.push_back("HOSTESS");
	for(int i=g.size()/2;i<g.size();i++){
		res.push_back(b[i]);
		res.push_back(g[i]);
	}
	return res;
}


