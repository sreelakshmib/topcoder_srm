class CalendarRecycle {
public:
	int useAgain(int);
	bool isleap(int y) {
		if(y%4==0 && y%100!=0) return true;
		if(y%400==0)return true;
		return false;
	}
};

int CalendarRecycle::useAgain(int year) {
	int rem=0,rem1=0,y=2003;
	y=year+1;
	while(true){
		if(isleap(y))rem++;
		rem += 365;
		cout<<rem<<" "<<y<<endl;
		if(rem%7==0 && isleap(y)==isleap(year))
			return y;
		y++;
	}
	return 0;
}


