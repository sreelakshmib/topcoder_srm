class Inchworm {
public:
	int lunchtime(int, int, int);
};

int Inchworm::lunchtime(int branch, int rest, int leaf) {
		int res=0;
	for(int i=0;i<=branch;i+=rest){
		if(i%leaf==0)
		res++;
	}
	return res;
}


