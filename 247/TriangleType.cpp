class TriangleType {
public:
	string type(int, int, int);
};

string TriangleType::type(int a, int b, int c) {
	VI res;
	res.pb(a);res.pb(b);res.pb(c);
	sort(full(res));
	if(res[0]+res[1]<=res[2])
		return "IMPOSSIBLE";
	if(res[0]*res[0]+res[1]*res[1]==res[2]*res[2])
		return "RIGHT";
	if(res[0]*res[0]+res[1]*res[1]>res[2]*res[2])
		return "ACUTE";
	if(res[0]*res[0]+res[1]*res[1]<res[2]*res[2])
		return "OBTUSE";
}


