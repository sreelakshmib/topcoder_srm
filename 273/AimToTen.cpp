class AimToTen {
public:
	int need(vector <int>);
};

int AimToTen::need(vector <int> marks) {
	int res=0,sum=0,n=marks.size();
	for(int i=0;i<marks.size();i++)
		sum+=marks[i];
	double avg=(double)sum/n;
	if(avg>=9.5)return 0;
	else{
		while(avg<9.5){
			sum+=10;
			res++;
			avg=(double)sum/(n+res);
			if(avg>=9.5) return res;
		}
	}
}


