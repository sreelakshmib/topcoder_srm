class BirthdayOdds {
public:
	int minPeople(int, int);
};

int BirthdayOdds::minPeople(int minOdds, int daysInYear) {
	double req=1-minOdds*1.0/100;
	double odd=1;
	int cnt=0;
	while(req<odd){
		++cnt;
		double d=1;
		for(int i=daysInYear,j=0;j<cnt;i--,j++){
			d*=i;
			d/=daysInYear;
		}
		odd=d;
	}
	return cnt;
}


