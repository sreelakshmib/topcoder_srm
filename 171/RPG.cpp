class RPG {
public:
	vector <int> dieRolls(vector <string>);
};

vector <int> RPG::dieRolls(vector <string> dice) {
	vector<int> r;
	int l=0,h=0;
	for(int i=0;i<dice.size();i++){
		stringstream s;
		int in=dice[i].find('d');
		cout<<in<<" ";
		dice[i][in]=' ';
		s<<dice[i];
		int n,x;
		cout<<dice[i]<<endl;
		s>>n>>x;
		l+=n;
		h+=n*x;
	}
	r.push_back(l);
	r.push_back(h);
	r.push_back((l+h)/2);
	return r;	
}


