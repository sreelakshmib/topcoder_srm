class NoOrderOfOperations {
public:
	int evaluate(string);
};

int NoOrderOfOperations::evaluate(string expr) {
	int res=expr[0]-'0';
	for(int i=1;i<expr.length();i+=2){
		int op=expr[i+1]-'0';
		switch(expr[i]){
			case '+':res+=op;break;
			case '-':res-=op;break;
			case '*':res*=op;break;
			case '/':res/=op;break;
		}
	}
	return res;
}


