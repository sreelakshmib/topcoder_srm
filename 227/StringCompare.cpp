class StringCompare {
public:
	int simpleDifference(string, string);
};

int StringCompare::simpleDifference(string a, string b) {
	int res=0;
	int len=min(a.length(),b.length());
	for(int i=0;i<len;i++)
		if(a[i]==b[i])res++;
	return res;

}


double test0() {
	string p0 = "TICK";
	string p1 = "TOCK";
	StringCompare * obj = new StringCompare();
	clock_t start = clock();
	int my_answer = obj->simpleDifference(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 3;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
