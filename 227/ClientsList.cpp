bool cmp(string a,string b){
	stringstream ss1(a);
	stringstream ss2(b);
	string f1,f2,l1,l2;
	ss1>>f1>>l1;
	ss2>>f2>>l2;
	if(l1.compare(l2)<0)
		return 1;
	else if(l1.compare(l2)==0){
		if(f1.compare(f2)<0)return 1;
		else return 0;
	}
	return 0;	
}

vector <string> ClientsList::dataCleanup(vector <string> names) {
	for(int i=0;i<names.size();i++){
		int pos=names[i].find(",");
		if(pos>=0 && pos<names[i].length()){
			names[i][pos]=' ';
			stringstream ss(names[i]);
			string a,b;
			ss>>a>>b;
			names[i]=b+" "+a;
		}
	}
	sort(names.begin(),names.end(),cmp);
	for(int i=0;i<names.size();i++){
		cout<<names[i]<<endl;
	}
	return names;
}


