class GoldenChain {
public:
	int minCuts(vector <int>);
};

int GoldenChain::minCuts(vector <int> s) {
	int links=0,n=s.size();
	sort(s.begin(),s.end());
	for(int i=0;i<n;i++){
		if(n-i-1>=s[i]+links)
			links+=s[i];
		else
			links+=max(n-links-i,0);
	}
	return links;
}


