	int t0[] = {3,3,3,3};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	GoldenChain * obj = new GoldenChain();
	clock_t start = clock();
	int my_answer = obj->minCuts(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
