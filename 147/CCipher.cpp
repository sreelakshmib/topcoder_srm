class CCipher {
public:
	string decode(string, int);
};

string CCipher::decode(string cipherText, int shift) {
		string res=cipherText;
	for(int i=0;i<res.length();i++){
		if(res[i]-shift>='A')res[i]-=shift;
		else{
			res[i]=res[i]-shift+26;
		}
	}
	return res;

}


