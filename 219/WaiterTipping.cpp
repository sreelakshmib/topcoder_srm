class WaiterTipping {
public:
	int maxPercent(int, int, int);
};

int WaiterTipping::maxPercent(int total, int taxPercent, int money) {
	for(int tip=1000000;tip>=0;tip--){
		if(total+ (total*taxPercent/100) + (total*tip/100) <=money)
			return tip;
	}
	return -1;
}


