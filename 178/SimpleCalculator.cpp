class SimpleCalculator {
public:
	int calculate(string);
};

int SimpleCalculator::calculate(string input) {
	int p=input.find('*');
	int a,b;
	if(p>=0 && p<=input.length()){
		stringstream s;		
		input[p]=' ';
		s<<input;
		s>>a>>b;
		return a*b;
	}
	p=input.find('+');
	if(p>=0 && p<=input.length()){
		stringstream s;		
		input[p]=' ';
		s<<input;
		s>>a>>b;
		return a+b;
	}
	p=input.find('-');
	if(p>=0 && p<=input.length()){
		stringstream s;		
		input[p]=' ';
		s<<input;
		s>>a>>b;
		return a-b;
	}
	p=input.find('/');
	if(p>=0 && p<=input.length()){
		stringstream s;		
		input[p]=' ';
		s<<input;
		s>>a>>b;
		return a/b;
	}
	return 0;	
	
}


double test0() {
	string p0 = "5/3";
	SimpleCalculator * obj = new SimpleCalculator();
	clock_t start = clock();
	int my_answer = obj->calculate(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
