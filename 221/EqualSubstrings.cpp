class EqualSubstrings {
public:
	vector <string> getSubstrings(string);
};

vector <string> EqualSubstrings::getSubstrings(string str) {
	vector<string> res;
	for(int i=str.length();i>=0;i--){
		string x=str.substr(str.length()-i);
		string y=str.substr(0,str.length()-i);
		if(count(x.begin(),x.end(),'a')!=count(y.begin(),y.end(),'b'))continue;
		res.push_back(x);
		res.push_back(y);
		return res;
	}
	return res;
	
	
	
}


