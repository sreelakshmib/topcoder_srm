class Substitute {
public:
	int getValue(string, string);
};

int Substitute::getValue(string key, string code) {
	int res=0;
	for(int i=0;i<code.length();i++){
		int p=key.find(code[i]);
		if(p>=0 && p<=9){
			res*=10;
			if(p==9)res;
			else res+=p+1;
		}		
	}
	return res;
	
	
}


