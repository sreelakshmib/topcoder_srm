class Multiples {
public:
	int number(int, int, int);
};

int Multiples::number(int min, int max, int factor) {
	int res=0;
	while(min%factor!=0)min++;
	while(max%factor!=0)max--;
	for(int i=min;i<=max;i+=factor){
		res++;
	}
	return res;
}


