class SpreadsheetColumn {
public:
	string getLabel(int);
};

string SpreadsheetColumn::getLabel(int column) {
	string res="";
	if(column<27)
		res+=column-1+'A';
	
	else {
		int first=0;
		while(column>26){
			first++;
			column-=26;
		}
		res+=first-1+'A';
		res+=column-1+'A';
	}
	return res;	
}


