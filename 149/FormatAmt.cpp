class FormatAmt {
public:
	string amount(int, int);
};

string FormatAmt::amount(int dollars, int cents) {
	string d;
	string c;
	string ans="$";
	string a="";
	stringstream ss;
	ss<<cents;ss>>c;
	if(cents==0)c="00";
	else if(cents<10)c="0"+c;
	string sep=",";
	string dot=".";
	if(dollars==0){
		if(cents==0){
			return "$0.00";
		}
		return ans+"0"+dot+c;
	} else{
		while(dollars>=1000){
			int x=dollars%1000;
			dollars/=1000;
			stringstream ss1;
			ss1<<x;ss1>>d;
			if(x==0)d="000";
			else if(x<10)d="00"+d;
			else if(x<100)d="0"+d;
			a=d+a;
			if(dollars)a=","+a;
		}
		stringstream ss1;
		ss1<<dollars;ss1>>d;
		a=d+a;
		return ans+a+dot+c;
	}
	return ans;
	
}


