class CalcTest {
public:
	vector <string> uniform(vector <string>);
};

vector <string> CalcTest::uniform(vector <string> numbers) {
	VS res(sz(numbers),"");
	rep(i,sz(numbers)){
		rep(j,len(numbers[i])){
			if(isdigit(numbers[i][j]))
				res[i]+=numbers[i][j];
			else if(numbers[i][j]==' ')
				continue;
			else 
				res[i]+='.';
		}
	}
	return res;
}


