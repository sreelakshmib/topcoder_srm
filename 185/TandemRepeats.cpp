class TandemRepeats {
public:
	int maxLength(string, int);
};
int diff(string f,string s){
	int ans=0;
	rep(i,len(f)){
		ans+= s[i]!=f[i];
	}
	return ans;
}

int TandemRepeats::maxLength(string dna, int k) {
	int ans=0,N=len(dna);
	rep(i,N){
		for(int j=1;i+2*j<=N;j++){
			string f=dna.substr(i,j);
			string s=dna.substr(i+j,j);
			if(diff(f,s)<=k)
				ans=max(j,ans);
		}
	}
	return ans;
}



