class LeapAge {
public:
	int getAge(int, int);
};

int isleap(int y){
	if(y%4==0){
		if(y%100==0){
			if(y%400==0)return 1;
			else return 0;
		}
		return 1;
	}
	return 0;
}

int LeapAge::getAge(int year, int born) {
	int res=0;
	for(int i=born+1;i<=year;i++){
		if(isleap(i))res++;
	}
	return res;
}


