class DinkyFish {
public:
	int monthsUntilCrowded(int, int, int);
};

int DinkyFish::monthsUntilCrowded(int tankVolume, int maleNum, int femaleNum) {
	int res=0;
	int f=femaleNum,m=maleNum,t=tankVolume;
	if(m+f>2*t)return 0;
	while(m+f<=2*t){
		res++;
		int p=min(m,f);
		m+=p;
		f+=p;	
	}	
	return res;
	
}


