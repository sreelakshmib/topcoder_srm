class TireRotation {
public:
	int getCycle(string, string);
};

int TireRotation::getCycle(string initial, string current) {
	vector<string> s;
	s.push_back(initial);
	for(int i=1;i<4;i++){
		string one=s[i-1];
		one[2]=s[i-1][0];
		one[3]=s[i-1][1];
		one[0]=s[i-1][3];
		one[1]=s[i-1][2];
		s.push_back(one);
	}
	int res=-1;
	for(int i=0;i<4;i++){
		if(s[i]==current)
			{res=i+1;break;}
	}
	return res;
	
}


