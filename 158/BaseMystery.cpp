map<char,int> m;

int checkNum(string num,int base){
	for(int i=0;i<num.length();i++){
		if(m[num[i]]>=base)
			return 0;
	}
	return 1;
}

int getNum(string num,int base){
	int res=0;
	for(int i=0;i<num.length();i++){
		res*=base;
		res+=m[num[i]];
	}
	return res;
}

int isBase(string e,int base){
	int p=e.find('+');
	e[p]=' ';
	p=e.find('=');
	e[p]=' ';
	stringstream s(e);
	string a1,b1,c1;
	s>>a1>>b1>>c1;
	int a,b,c;
	if(checkNum(a1,base) && checkNum(b1,base) && checkNum(c1,base)){
		int a=getNum(a1,base);
		int b=getNum(b1,base);
		int c=getNum(c1,base);
		if(a+b==c){
			return 1;
		}
	}
	return 0;
}

vector <int> BaseMystery::getBase(string equation) {
	VI res;
	for(int i=0;i<10;i++)
		m[i+'0']=i;
	int x=10;
	for(char i='A';i<='J';i++,x++)
		m[i]=x;
	for(int i=1;i<=20;i++){
		if(isBase(equation,i))
			res.pb(i);
	}
	return res;

}


