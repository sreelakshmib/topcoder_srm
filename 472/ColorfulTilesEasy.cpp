class ColorfulTilesEasy {
public:
	int theMin(string);
};

int ColorfulTilesEasy::theMin(string room) {
	int res=0;
	for(int i=0;i<room.length()-1;i++){
		if(room[i]==room[i+1]){
			res++;
			room[i+1]='a';
		}
	}
	return res;
}


