class LCMRange {
public:
	int lcm(int, int);
};

int LCMRange::lcm(int first, int last) {
		int res=1;
	for(int i=1;i<=27720;i++){
		int cnt=0;
		for(int j=first;j<last+1;j++){
			if(i%j!=0)break;
			cnt++;
		}
		if(cnt==last-first+1)return i;
	}
	return 0;	

}


