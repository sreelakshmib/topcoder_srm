class ClassScores {
public:
	vector <int> findMode(vector <int>);
};

vector <int> ClassScores::findMode(vector <int> scores) {
	map<int,int> m;
	map<int,int>::iterator it;
	rep(i,sz(scores)){
			m[scores[i]]+=1;
	}
	VI res;
	int max=-1;
	for(it=m.begin();it!=m.end();it++){
		if((*it).second>max)
			max=(*it).second;
	}
	for(it=m.begin();it!=m.end();it++){
		if((*it).second==max)
			res.pb((*it).first);
	}
	return res;
}


