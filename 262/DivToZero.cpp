class DivToZero {
public:
	string lastTwo(int, int);
};

string DivToZero::lastTwo(int num, int factor) {
	int last2=num%100;
	num-=last2;
	int add=0;
	while(num%factor){
		num++;
		add++;
	}
	stringstream ss;
	ss<<add;
	string res;
	ss>>res;
	if(res.length()<2)
		res="0"+res;
	return res;
}


