class MassiveNumbers {
public:
	string getLargest(string, string);
};

string MassiveNumbers::getLargest(string numberA, string numberB) {
	string nA=numberA;
	string nB=numberB;
	int p=nA.find('^');
	nA[p]=' ';
	p=nB.find('^');
	nB[p]=' ';
	stringstream s1(nA),s2(nB);
	int a,b,c,d;
	s1>>a>>b;
	s2>>c>>d;
	int m=b*log(a);
	int n=d*log(c);
	if(m>n)
		return numberA;
	else
		return numberB;
}


double test0() {
	string p0 = "3^100";
	string p1 = "2^150";
	MassiveNumbers * obj = new MassiveNumbers();
	clock_t start = clock();
	string my_answer = obj->getLargest(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p2 = "3^100";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p2 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
