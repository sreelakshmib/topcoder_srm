class VLNString {
public:
	string makeAcronym(string);
};

string VLNString::makeAcronym(string longName) {
	stringstream ss;
	ss<<longName;
	string res="";
	string tmp;
	while(ss>>tmp){
		if(tmp!="of" && tmp!="the" && tmp!="and")
			res+=toupper(tmp[0]);
	}
	return res;
}


