	Stairs * obj = new Stairs();
	clock_t start = clock();
	int my_answer = obj->designs(p0, p1, p2, p3);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p4 = 1;
