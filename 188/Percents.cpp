class Percents {
public:
	int minSamples(string);
};

int Percents::minSamples(string percent) {
	int c=0;
	c=percent[0]-'0';c*=10;
	c+=percent[1]-'0';c*=10;
	c+=percent[3]-'0';c*=10;
	c+=percent[4]-'0';
	for(int i=1;i<=10000;i++){
		for(int j=0;j<=i;j++){
			int n=(int)(j*10000.0/i+0.5);
			if(n==c)
				return i;
		}
	}
	return-1;
}


double test0() {
	string p0 = "25.00%";
	Percents * obj = new Percents();
	clock_t start = clock();
	int my_answer = obj->minSamples(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 4;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
