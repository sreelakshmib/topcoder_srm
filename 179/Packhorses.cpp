class Packhorses {
public:
	int horses(int, int, int);
};

int Packhorses::horses(int p, int x, int y) {
	int res=10000;
	for(int i=0;i<=p;i++){
		int xx=max(0,x-i*2);
		int yy=max(0,y-p+i);
		res=min(res,p+(xx+2)/3+(yy+1)/2);
	}
	return res;
}


double test0() {
	int p0 = 1;
	int p1 = 5;
	int p2 = 0;
	Packhorses * obj = new Packhorses();
	clock_t start = clock();
	int my_answer = obj->horses(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
