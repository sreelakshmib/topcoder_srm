class BritishCoins {
public:
	vector <int> coins(int);
};

vector <int> BritishCoins::coins(int pence) {
	VI res;
	res.pb(pence/(12*20));
	pence%=(12*20);
	res.pb(pence/12);
	pence%=12;
	res.pb(pence);
	return res;
	
}


