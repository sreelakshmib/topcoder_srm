class StreetParking {
public:
	int freeParks(string);
};

int StreetParking::freeParks(string street) {
	street = "*" + street + "**";
	int count=0;
	for (int i=1; i<street.length()-2; i++) {
      char current = street[i];
      char previous = street[i-1];
      char next = street[i+1];
      char next2 = street[i+2];

      if (current=='-' &&
         next!='B' && next2!='B' &&
         previous!='S' && next!='S')
         count++;
   }
   return count;
}


